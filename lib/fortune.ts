export function calculateTypeNumber(birthdate: string): number {
  const digits = birthdate.replaceAll("-", "").split("").map(Number);
  let sum = digits.reduce((a, b) => a + b, 0);
  while (sum > 9) {
    sum = String(sum)
      .split("")
      .map(Number)
      .reduce((a, b) => a + b, 0);
  }
  return sum === 0 ? 9 : sum;
}

function concernFragment(concern?: string): string {
  const clean = (concern ?? "").replace(/\s+/g, " ").trim();
  if (!clean) {
    return "今の迷いは、まだ言葉になりきっていない部分も含めて、あなたの中で大切なサインになっています。";
  }
  const excerpt = clean.length > 54 ? `${clean.slice(0, 54)}...` : clean;
  return `「${excerpt}」という迷いには、焦りだけでなく、本当は丁寧に選びたい気持ちが隠れています。`;
}

